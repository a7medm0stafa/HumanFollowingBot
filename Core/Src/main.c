    if(leftDistance>40 && rightDistance>40){
    	TIM2->CCR1=0;
    	TIM2->CCR2=0;
    }
    else if(leftDistance>rightDistance && rightDistance>5){
    	error=desiredDistance-rightDistance;

    	TIM2->CCR1=1000*error;


    }
    else if(leftDistance<rightDistance  && leftDistance>5){
    	error=desiredDistance-leftDistance;
    	TIM2->CCR2=1000*error;


    }
